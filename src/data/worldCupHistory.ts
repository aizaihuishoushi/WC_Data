// 获取球队历史数据
export function getTeamHistory(teamCode: string): TeamHistory | undefined {
  return teamsHistoryData[teamCode];
}

// 获取球队所有历史比赛
export function getTeamMatches(teamCode: string): WorldCupMatch[] {
  const allMatches: WorldCupMatch[] = [];
  
  for (const [yearStr, matches] of Object.entries(worldCupHistory)) {
    const year = parseInt(yearStr);
    const teamMatches = matches.filter(
      m => m.team1 === teamCode || m.team2 === teamCode
    );
    allMatches.push(...teamMatches);
  }
  
  return allMatches.sort((a, b) => b.year - a.year);
}

// 获取所有世界杯年份
export function getWorldCupYears(): number[] {
  return Object.keys(worldCupHistory)
    .map(y => parseInt(y))
    .sort((a, b) => b - a);
}

// 获取某届世界杯的决赛信息
export function getFinalMatch(year: number): WorldCupMatch | undefined {
  const matches = worldCupHistory[year];
  if (!matches) return undefined;
  return matches.find(m => m.round === 'Final');
}

// 获取某届世界杯的冠军
export function getChampion(year: number): string | undefined {
  const final = getFinalMatch(year);
  if (!final) return undefined;
  if (!final.score?.ft) return undefined;
  
  const [home, away] = final.score.ft;
  if (home > away) return final.team1;
  if (away > home) return final.team2;
  return undefined; // 点球大战，需要额外处理
}
