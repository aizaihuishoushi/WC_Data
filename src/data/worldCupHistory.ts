export interface WorldCupMatch {
  year: number;
  date: string;
  round: string;
  team1: string;
  team2: string;
  score?: {
    ft?: [number, number];      // 全场比分
    et?: [number, number];     // 加时赛
    p?: [number, number];      // 点球
  };
  stadium?: string;
  city?: string;
  country?: string;
}

export interface TeamHistory {
  code: string;
  name: string;
  logo: string;
  appearances: number[];           // 参赛年份列表
  bestResult: {
    year: number;
    stage: string;
  };
  totalMatches: number;
  totalWins: number;
  totalDraws: number;
  totalLosses: number;
  totalGoalsFor: number;
  totalGoalsAgainst: number;
  championsYears: number[];       // 夺冠年份
  finalsAppearances: number[];    // 决赛参赛年份
  semiFinalsAppearances: number[]; // 四强参赛年份
  matches: TeamMatchRecord[];
}

export interface TeamMatchRecord {
  year: number;
  opponent: string;
  score: string;
  result: 'win' | 'draw' | 'loss';
  round: string;
  date: string;
}

